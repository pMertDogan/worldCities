    private void populateTable() {
        model = (DefaultTableModel) tblCities.getModel();
        //güncelleme yapınca eski değerleri sil
        model.setRowCount(0);
        try {
            ArrayList<City> sehirler = getCities();
            
            for ( City city:sehirler){
                
                Object[] row = {city.getId(),city.getName()
                        ,city.getCountryCode(),city.getDistrict(),city.getPopulation()};
                
                model.addRow(row);
            }
            
            
            
        } catch (SQLException ex) {
            
        }
    }
    
    public ArrayList<City> getCities() throws SQLException{
        
        Connection connection = null;
        DBHelper dBHelper = new DBHelper();
        Statement statement = null;
        ResultSet resultSet ;
        ArrayList<City> sehirler = null;
        try{
            connection = dBHelper.getConnection();
            statement = connection.createStatement();
            resultSet = statement.executeQuery("select * from City");
            sehirler = new ArrayList<City>();
            while(resultSet.next()){
            sehirler.add(new City(
                    resultSet.getInt("ID"),
                    resultSet.getInt("Population"),
                    resultSet.getString("Name"),
                    resultSet.getString("CountryCode"),
                    resultSet.getString("District")
                   ));
            }   
    }catch(SQLException exception){
    dBHelper.showErrorMesseage(exception);
    }finally{
       connection.close();
       }
    return sehirler;   
    
    }

