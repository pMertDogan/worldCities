    private void btnEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEkleActionPerformed


        Connection connection = null;
        DBHelper helper = new DBHelper();
        PreparedStatement statement= null;
        
        try{
        connection = helper.getConnection();
        String sql = "insert into City (Name,CountryCode,District,Population) values (?,?,?,?)";
        statement = connection.prepareStatement(sql);
        statement.setString(1, txtFieldName.getText());
        statement.setString(2, txtFieldCountry.getText());
        statement.setString(3, txtFieldDistrcit.getText());
        statement.setInt(4, Integer.valueOf(txtFieldPopulation.getText()));
        int result = statement.executeUpdate();
        }catch(SQLException exception){
            helper.showErrorMesseage(exception);
    }finally{
            try {
                statement.close();
                connection.close();
                populateTable();
            } catch (SQLException ex) {
                  helper.showErrorMesseage(ex);
            }
        }

    }//GEN-LAST:event_btnEkleActionPerformed

